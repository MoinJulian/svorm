<h1>Svorm</h1>

<p class="teaser">Create forms and share them with others.</p>

<p>
	This app allows you to create forms – called svorms –, which are made up out of simple questions
	and multiple choices. Share the link with others who can then answer the svorm. The results of
	each svorm can also be seen.
</p>
<p>Please notice that svorms which are older than a month will be deleted regularly.</p>
<p>
	You can find the code of this app on <a href="https://github.com/MoinJulian/svorm">GitHub</a>
</p>

